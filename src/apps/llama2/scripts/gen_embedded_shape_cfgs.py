#!/usr/bin/env python3
import csv
from pathlib import Path


ROOT = Path(__file__).resolve().parents[4]
OUT = ROOT / "src/apps/llama2/embedded_shape_cfgs.inc"

CSV_SPECS = [
    ("tmp/llama_prefill_best_binary_edge.csv", 0),
    ("tmp/llama_prefill_best_int2_edge.csv", 2),
    ("tmp/llama_prefill_best_int4_edge.csv", 3),
]


def emit_entry(row: list[str], prec: int) -> str:
    return (
        "    {.M = %sUL, .N = %sUL, .K = %sUL, .cfg = {%sUL, %sUL, %sUL, %sUL, %sUL, %dUL}, "
        ".total_cycles = %sLL, .compute_cycles = %sLL},"
        % (
            row[0],
            row[1],
            row[2],
            row[8],
            row[9],
            row[10],
            row[4],
            row[5],
            prec,
            str(int(round(float(row[25])))),
            str(int(round(float(row[26])))),
        )
    )


def main() -> None:
    lines = [
        "/* Auto-generated by src/apps/llama2/scripts/gen_embedded_shape_cfgs.py. */",
    ]

    for rel_path, prec in CSV_SPECS:
        path = ROOT / rel_path
        with path.open(newline="") as f:
            reader = csv.reader(f)
            next(reader)
            rows = list(reader)
        lines.append(f"/* {rel_path} */")
        for row in rows:
            lines.append(emit_entry(row, prec))

    OUT.write_text("\n".join(lines) + "\n")


if __name__ == "__main__":
    main()
